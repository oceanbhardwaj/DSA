// arr[] : int input array of integers
// k : the quadruple sum required

class Solution {
    public ArrayList<ArrayList<Integer>> fourSum(int[] arr, int k1) {
       ArrayList<ArrayList<Integer>> res=new ArrayList<>();
       int n=arr.length;
       for(int i=0;i<n;i++){
           for(int j=i+1;j<n;j++){
               for(int k=j+1;k<n;k++){
                   for(int l=k+1;l<n;l++){
                       if(arr[i]+arr[j]+arr[k]+arr[l]==k1)
                       {
                           ArrayList<Integer> a=new ArrayList<>();
                           a.add(arr[i]);
                           a.add(arr[j]);
                           a.add(arr[k]);
                           a.add(arr[l]);
                           Collections.sort(a);
                           if(!res.contains(a))
                           res.add(a);
                           
                       }
                   }
               }
           }
       }
       return res;
    }
}